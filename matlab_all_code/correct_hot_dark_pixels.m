function [neighborRows, neighborCols] = findGoodNeighbors(row, col, ny, nx, hotPixelMask)
    neighborRows = [];
    neighborCols = [];

    for radius = 1:4
        rowRange = max(1, row - radius):min(ny, row + radius);
        colRange = max(1, col - radius):min(nx, col + radius);
        [rr, cc] = ndgrid(rowRange, colRange);

        isSelf = (rr == row) & (cc == col);
        isGood = ~isSelf & ~hotPixelMask(rowRange, colRange);

        neighborRows = rr(isGood);
        neighborCols = cc(isGood);

        if ~isempty(neighborRows)
            return;
        end
    end
end
