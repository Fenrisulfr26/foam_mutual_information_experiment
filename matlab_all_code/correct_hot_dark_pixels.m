function [histOut, info] = correct_hot_dark_pixels(histIn, hotPixelMask)
%CORRECT_HOT_DARK_PIXELS Replace fixed high-dark-noise SPAD pixels.
%
% Usage:
%   histOut = correct_hot_dark_pixels(histIn)
%   [histOut, info] = correct_hot_dark_pixels(histIn)
%   histOut = correct_hot_dark_pixels(histIn, hotPixelMask)
%
% Input:
%   histIn:
%       ny x nx x nBins photon histogram. For the current camera this is
%       usually 32 x 32 x 227.
%
%   hotPixelMask:
%       Optional ny x nx logical mask. true means the pixel should be
%       replaced. If omitted, the fixed 32 x 32 mask detected from
%       data/time_correction/uniform_ref.mat is used.
%
% Output:
%   histOut:
%       Same size as histIn. Hot pixels are replaced by the average of
%       surrounding non-hot pixels, independently for every time bin.
%
%   info:
%       Struct containing the mask, pixel list, and neighbor count used for
%       each replaced pixel.

    if nargin < 1
        error('Usage: histOut = correct_hot_dark_pixels(histIn)');
    end

    if ndims(histIn) ~= 3
        error('histIn must be a 3D array: ny x nx x nBins.');
    end

    [ny, nx, nBins] = size(histIn);

    if nargin < 2 || isempty(hotPixelMask)
        hotPixelMask = defaultHotPixelMask(ny, nx);
    else
        if ~isequal(size(hotPixelMask), [ny, nx])
            error('hotPixelMask size must match the first two dimensions of histIn.');
        end
        hotPixelMask = logical(hotPixelMask);
    end

    histOut = histIn;
    [hotRows, hotCols] = find(hotPixelMask);
    neighborCount = zeros(numel(hotRows), 1);

    for k = 1:numel(hotRows)
        row = hotRows(k);
        col = hotCols(k);

        [neighborRows, neighborCols] = findGoodNeighbors(row, col, ny, nx, hotPixelMask);
        neighborCount(k) = numel(neighborRows);

        if isempty(neighborRows)
            warning('No valid neighbor found for hot pixel row=%d col=%d. Pixel is unchanged.', row, col);
            continue;
        end

        neighborCurves = zeros(numel(neighborRows), nBins, 'like', histIn);
        for j = 1:numel(neighborRows)
            neighborCurves(j, :) = reshape(histIn(neighborRows(j), neighborCols(j), :), 1, []);
        end

        histOut(row, col, :) = reshape(mean(neighborCurves, 1), 1, 1, []);
    end

    info = struct();
    info.hotPixelMask = hotPixelMask;
    info.hotPixels = [hotRows, hotCols];
    info.numHotPixels = numel(hotRows);
    info.neighborCount = neighborCount;
    info.method = 'Each hot pixel is replaced by the time-bin-wise average of surrounding non-hot pixels.';
end

